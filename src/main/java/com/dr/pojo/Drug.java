package com.dr.pojo;

public class Drug {
	
	private int drugNo;
	private String drugName;
	public int getDrugNo() {
		return drugNo;
	}
	public void setDrugNo(int drugNo) {
		this.drugNo = drugNo;
	}
	public String getDrugName() {
		return drugName;
	}
	public void setDrugName(String drugName) {
		this.drugName = drugName;
	}

	
}
