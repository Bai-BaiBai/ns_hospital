package com.dr.pojo;

public class ExportStore {

	private int exportId;
	private int drugId;
	private double exportNum;
	private double exportPrice;
	private String transman;
	public int getExportId() {
		return exportId;
	}
	public void setExportId(int exportId) {
		this.exportId = exportId;
	}
	public int getDrugId() {
		return drugId;
	}
	public void setDrugId(int drugId) {
		this.drugId = drugId;
	}
	public double getExportNum() {
		return exportNum;
	}
	public void setExportNum(double exportNum) {
		this.exportNum = exportNum;
	}
	public double getExportPrice() {
		return exportPrice;
	}
	public void setExportPrice(double exportPrice) {
		this.exportPrice = exportPrice;
	}
	public String getTransman() {
		return transman;
	}
	public void setTransman(String transman) {
		this.transman = transman;
	}

	
}
